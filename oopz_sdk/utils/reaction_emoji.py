def normalize_reaction_emoji(value: str | int) -> str:
    """
    将用户输入归一化为接口需要的真实 emoji 字符。

    支持：
    - 真实 emoji: "🫢"
    - 十进制 unicode code point: 129762 / "129762"
    - 十六进制 unicode code point: "1FAE2" / "U+1FAE2" / "10320E" / "U+10320E"
    """
    if value is None:
        raise ValueError("emoji is required")

    raw = str(value).strip()
    if raw == "":
        raise ValueError("emoji is required")

    # 用户直接传真实 emoji
    emoji = _normalize_real_emoji(raw)
    if emoji is not None:
        return emoji

    # 用户传十进制 unicode code point: 129762 / "129762"
    if raw.isdigit() and raw in emoji_list:
        return chr(int(raw))

    # 用户传十六进制 unicode code point: "10320E" / "U+10320E"
    hex_raw = raw.upper()
    if hex_raw.startswith("U+"):
        hex_raw = hex_raw[2:]

    try:
        codepoint = int(hex_raw, 16)
    except ValueError:
        codepoint = None

    if codepoint is not None:
        code = str(codepoint)
        if code in emoji_list:
            return chr(codepoint)

    raise ValueError(f"unsupported reaction emoji: {value!r}")


def _normalize_real_emoji(raw: str) -> str | None:
    """
    校验真实 emoji 是否在允许列表里。

    会处理：
    - 普通单 code point emoji，例如 "🫢"
    - 带 VS16 的 emoji，例如 "❤️"
    """
    if len(raw) == 1:
        code = str(ord(raw))
        if code in emoji_list:
            return raw

    # 去掉 variation selector，例如 ❤️ -> ❤
    compact = raw.replace("\ufe0f", "")
    if len(compact) == 1:
        code = str(ord(compact))
        if code in emoji_list:
            return chr(int(code))

    return None


